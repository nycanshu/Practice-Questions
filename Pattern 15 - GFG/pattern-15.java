class Solution {

    void printTriangle(int n) {
        // code here
        for(int line=1;line<=n;line++){
            char ch ='A';
            for(int value=n;value>=line;value--){
                System.out.print(ch);
                ch++;
            }
            System.out.println();
        }
    }
}