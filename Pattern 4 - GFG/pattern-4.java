class Solution {

    void printTriangle(int n) {
        // code here
        int num =1;
        for(int line =1;line<=n;line++){
            for(int value = 1; value<=line;value++){
                System.out.print(num +" ");
            }
            System.out.println();
            num++;
        }
    }
}